package com.example.calculator;

public class Operation {

    /**
     * Addition
     *
     * @param a
     * @param b
     * @return
     */
    public static int add(int a, int b) {
        return a + b;
    }

    /**
     * Soustraction
     *
     * @param a
     * @param b
     * @return
     */
    public static int sub(int a, int b) {
        return a * b;
    }
}
